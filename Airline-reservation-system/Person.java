public class Person {
    String name;
    String nationality;
    String dob;
    String[] passport;
    int seatNumber;

    public Person(String name, String nationality, String dob, String[] passport, int seatNumber){
        this.name = name;
        this.nationality = nationality;
        this.dob = dob;
        this.passport = passport;
        this.seatNumber = seatNumber;
    }
}
